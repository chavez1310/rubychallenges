weather = "sunny"

case(weather)
when "sunny" 
	puts "Bust out those shades!"
when "cloudy" 
	puts "Put on that sweater!"
when "foggy" 
	puts "Better bring a flashlight, to see through that fog!"
when "rainy" 
	puts "You should probably bring that umbrella."
else
	puts "Hmm, just look out the window and follow the crowd's lead, i've got nothing for you."
end